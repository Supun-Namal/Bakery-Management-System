    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            // Get category from ComboBox instead of TextField
            String selectedCategory = cmbCategory.getSelectedItem().toString();

            // Validation: Ensure a category is actually selected
            if (selectedCategory.equals("-- Select Item --")) {
                JOptionPane.showMessageDialog(this, "Please Select a Category");
                return;
            }

            Product p = new Product(
                    txtName.getText().trim(),
                    selectedCategory, // Use the variable here
                    Double.parseDouble(txtPrice.getText().trim()),
                    Integer.parseInt(txtQty.getText().trim()),
                    Integer.parseInt(txtAlert.getText().trim())
            );

            boolean ok;
            if (editingProductId == null) {
                ok = controller.addProduct(p);
            } else {
                p.setProductId(editingProductId);
                ok = controller.updateProduct(p);
            }

            if (ok) {
                JOptionPane.showMessageDialog(this, "Saved Successfully");
                loadProducts();
                clearForm();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Please Complete the Form");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

