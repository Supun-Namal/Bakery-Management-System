    private void loadProducts() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);

        List<Product> list = controller.getAllProducts();
        for (Product p : list) {

            // ðŸš« skip hidden products
            if (hiddenProductIds.contains(p.getProductId())) {
                continue;
            }

            model.addRow(new Object[]{
                p.getProductId(),
                p.getName(),
                p.getCategory(),
                p.getPrice(),
                p.getQty(),
                p.getAlertLevel()
            });
        }
    }

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a Product to Edit");
            return;
        }

        editingProductId = (int) table.getValueAt(row, 0);

        txtName.setText(table.getValueAt(row, 1).toString());

        // Set the ComboBox value to match the table value
        String category = table.getValueAt(row, 2).toString();
        cmbCategory.setSelectedItem(category);

        txtPrice.setText(table.getValueAt(row, 3).toString());
        txtQty.setText(table.getValueAt(row, 4).toString());
        txtAlert.setText(table.getValueAt(row, 5).toString());

        btnSave.setText("Update");
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = table.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a Product");
            return;
        }

        int id = (int) table.getValueAt(row, 0);

        int c = JOptionPane.showConfirmDialog(
                this,
                "Are You Sure You Want to Delete?",
                "Confirm",
                JOptionPane.YES_NO_OPTION
        );

        if (c == JOptionPane.YES_OPTION) {
            hiddenProductIds.add(id); // âœ… remember hidden product
            ((DefaultTableModel) table.getModel()).removeRow(row); // hide now
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

