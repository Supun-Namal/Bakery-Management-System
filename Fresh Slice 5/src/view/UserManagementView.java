    private void showRestockAlerts() {
        List<Product> list = controller.getLowStockProducts();
        StringBuilder sb = new StringBuilder();
        sb.append("âš  Low Stock Items âš \n\n");

        boolean hasItems = false;

        for (Product p : list) {
            if (hiddenProductIds.contains(p.getProductId())) {
                continue; // ðŸ”¥ skip hidden products
            }
            hasItems = true;
            sb.append(p.getName())
                    .append(" | Qty: ")
                    .append(p.getQty())
                    .append(" | Alert: ")
                    .append(p.getAlertLevel())
                    .append("\n");
        }

        if (!hasItems) {
            txtArea.setText("No items need restocking.");
        } else {
            txtArea.setText(sb.toString());
        }
    }
