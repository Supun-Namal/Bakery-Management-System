    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        String firstName = txtFirstName.getText().trim();
        String lastName = txtLastName.getText().trim();
        String email = txtEmail.getText().trim();
        String u = txtUserName.getText().trim();
        String p = new String(txtPassword.getPassword()).trim();
        String r = (String) cmbRole.getSelectedItem();

        if (firstName.isEmpty() || lastName.isEmpty() || email.isEmpty()
                || u.isEmpty() || p.isEmpty()
                || r.equals("--Select Role--")) {

            JOptionPane.showMessageDialog(this, "Fill All Fields");
            return;
        }

        // ðŸ”’ ROLE FIXED AS ASSISTANT
        User user = new User(u, p, firstName, lastName, email);

        if (userController.createUser(user)) {
            JOptionPane.showMessageDialog(this, "Assistant Created Successfully");

            txtFirstName.setText("");
            txtLastName.setText("");
            txtEmail.setText("");
            txtUserName.setText("");
            txtPassword.setText("");
            cmbRole.setSelectedIndex(0);

        } else {
            JOptionPane.showMessageDialog(this, "Username Already Exists");
        }
    }//GEN-LAST:event_btnCreateActionPerformed

